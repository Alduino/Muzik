export function colour<Name extends keyof typeof colours>(
    name: Name,
    shade: keyof (typeof colours)[Name],
    opacity?: number
): string {
    const colour = colours[name][shade];

    if (opacity) {
        return `hsla(${colour} / ${opacity * 100}%)`;
    } else {
        return `hsl(${colour})`;
    }
}

export function alpha(colour: string, opacity: number): string {
    return colour.replace(
        /hsl\((\d+) (\d+%) (\d+%)\)/g,
        `hsl($1 $2 $3 / ${opacity * 100}%)`
    );
}
